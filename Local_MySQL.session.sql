select EMP.DEPT_ID, EMP.NAME, DEPT.DEPT_NAME FROM EMP LEFT JOIN DEPT ON EMP.DEPT_ID = DEPT.DEPT_ID;
select EMP.DEPT_ID, EMP.NAME, DEPT.DEPT_NAME FROM EMP RIGHT JOIN DEPT ON EMP.DEPT_ID = DEPT.DEPT_ID;

-- FULL OUTER JOIN DOES NOT SUPPORTS IN MYSQL, TO USE IT WE NEED TO FIRST FIND LEFT JOIN(OR LEFT OUTER JOIN) AND RIGHT JOIN(OR RIGHT OUTER JOIN) AND THEN USE UNION TO COMBINE THEM AND GET THE RESULT