CREATE TABLE EMP(
	EMP_ID INT PRIMARY KEY AUTO_INCREMENT,
    NAME VARCHAR(255),
    DEPT_ID INT
);
    
CREATE TABLE DEPT(
	DEPT_ID INT,
    DEPT_NAME VARCHAR(255)
    );
    
